<script>
  export default {
    name: 'Register',
    data() {
      return {
        // 手机号
        phone:'',
        // 验证码
        code:'',
        password:'',
        // 确认密码
        password_test:'',
        // 是否同意协议
        agree:true
      }
    },
    methods: {
    // 获取验证码
      async getCode() {
        try {
          const {phone} = this
          phone && (await this.$store.dispatch('getCode', phone))
          this.code = this.$store.state.user.code
        } catch (error) {
          alert(error.message)
        }
      },
      async userRegister(){
        const success = await this.$validator.validateAll()
        // 所有表单验证成果再请求服务器数据
        if(success) {
          try {
          // 如果成功跳转路由
          const {phone, code, password, password_test} = this;
          await this.$store.dispatch('userRegister', {phone, code, password})
          this.$router.push('/login')
          } catch (error) {
            alert(error.message)
          }
        }
      }
    }
  }
</script>

